#!/usr/bin/env node
// Generate docs/reference/cli.md from the kb CLI's SUBCOMMANDS registry.
//
// SUBCOMMANDS (command name + summary) and each subcommand's `*_HELP` body live
// in src/cli.ts / src/cli-<name>.ts. They are not exported, so rather than edit
// CLI source we read the same source of truth through the built CLI itself:
//   - `kb help --format=json` -> ordered { name, summary } for every command
//   - `kb help <name>`         -> that command's verbatim `*_HELP` body
// Reading the registry dynamically (instead of a hand-maintained list) is what
// lets the drift gate catch a newly added subcommand. Output is deterministic
// (commands sorted by name) so the gate is not noisy.
//
// Modes:
//   node scripts/gen-cli-reference.mjs           # write docs/reference/cli.md
//   node scripts/gen-cli-reference.mjs --check   # exit 1 if the committed doc drifts
//
// Mirrors scripts/generate-config-reference.mjs / scripts/check-config-reference.mjs.

import * as fs from 'node:fs/promises';
import * as fsSync from 'node:fs';
import * as os from 'node:os';
import * as path from 'node:path';
import { spawnSync } from 'node:child_process';
import { fileURLToPath, pathToFileURL } from 'node:url';

export const CLI_REFERENCE_PATH = 'docs/reference/cli.md';

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const CLI_BIN = 'build/cli.js';

// Capture the built CLI's stdout via a temp file (synchronous file writes) rather
// than a pipe: cli.js exits with process.exit(), which can truncate async piped
// stdout for larger payloads like the JSON help manifest.
function runCli(root, args) {
  const cliPath = path.join(root, CLI_BIN);
  if (!fsSync.existsSync(cliPath)) {
    throw new Error(`${CLI_BIN} not found; run \`npm run build\` first.`);
  }
  const tmp = path.join(os.tmpdir(), `kb-cli-reference-${process.pid}-${runCli.seq++}.txt`);
  const fd = fsSync.openSync(tmp, 'w');
  try {
    const result = spawnSync(process.execPath, [cliPath, ...args], {
      stdio: ['ignore', fd, 'pipe'],
      maxBuffer: 64 * 1024 * 1024,
    });
    if (result.error) throw result.error;
    if (result.status !== 0) {
      const stderr = result.stderr ? result.stderr.toString() : '';
      throw new Error(`kb ${args.join(' ')} exited ${result.status}: ${stderr.trim()}`);
    }
  } finally {
    fsSync.closeSync(fd);
  }
  try {
    return fsSync.readFileSync(tmp, 'utf8');
  } finally {
    fsSync.rmSync(tmp, { force: true });
  }
}
runCli.seq = 0;

export function generateCliReferenceMarkdown({ root = REPO_ROOT } = {}) {
  const manifest = JSON.parse(runCli(root, ['help', '--format=json']));
  const commands = [...manifest.commands].sort((a, b) => a.name.localeCompare(b.name, 'en'));

  // A single fence width that is safe for every body keeps the output uniform.
  const bodies = commands.map((command) => normalizeBody(runCli(root, ['help', command.name])));
  const ticks = fenceTicks(bodies);
  const openFence = `${ticks}text`;
  const closeFence = ticks;

  const lines = [
    '# CLI Reference',
    '',
    '<!-- This file is generated by scripts/gen-cli-reference.mjs. Do not edit by hand. -->',
    '',
    'This reference is generated from the `SUBCOMMANDS` registry in `src/cli.ts`',
    "(command names and summaries) and each subcommand's `--help` body. It is",
    'produced by invoking the built CLI so it always reflects the shipped command',
    'surface. Run `npm run build && node scripts/gen-cli-reference.mjs` after',
    'changing a command or its help text, and commit the result. The',
    '`docs:check-cli` gate (part of `npm run check`) fails if this file drifts.',
    '',
    `The \`kb\` CLI exposes ${commands.length} commands.`,
    '',
    '## Commands',
    '',
    '| Command | Summary |',
    '| --- | --- |',
    ...commands.map(
      (command) =>
        `| [\`kb ${command.name}\`](#kb-${slugSuffix(command.name)}) | ${tableCell(command.summary)} |`,
    ),
  ];

  commands.forEach((command, index) => {
    lines.push(
      '',
      `## \`kb ${command.name}\``,
      '',
      command.summary,
      '',
      openFence,
      ...bodies[index].split('\n'),
      closeFence,
    );
  });

  return `${lines.join('\n')}\n`;
}

export async function writeCliReference({ root = REPO_ROOT } = {}) {
  const target = path.join(root, CLI_REFERENCE_PATH);
  await fs.mkdir(path.dirname(target), { recursive: true });
  await fs.writeFile(target, generateCliReferenceMarkdown({ root }), 'utf8');
}

export async function checkCliReference({ root = REPO_ROOT } = {}) {
  const expected = generateCliReferenceMarkdown({ root });
  const target = path.join(root, CLI_REFERENCE_PATH);
  let actual = null;
  try {
    actual = await fs.readFile(target, 'utf8');
  } catch (err) {
    if (err.code !== 'ENOENT') throw err;
  }
  return { ok: actual === expected, exists: actual !== null };
}

function normalizeBody(body) {
  // Drop trailing whitespace (the closing fence re-adds a newline) so the output
  // is stable regardless of how a `*_HELP` string terminates.
  return body.replace(/\s+$/u, '');
}

function fenceTicks(bodies) {
  let maxRun = 0;
  for (const body of bodies) {
    for (const match of body.matchAll(/`+/g)) {
      maxRun = Math.max(maxRun, match[0].length);
    }
  }
  return '`'.repeat(Math.max(3, maxRun + 1));
}

// GitHub slugifies `## \`kb import-url\`` to `kb-import-url`: backticks are
// dropped and spaces become hyphens. Command names are already lowercase and
// only contain letters/hyphens, so the suffix is the name verbatim.
function slugSuffix(name) {
  return name;
}

function tableCell(value) {
  return value.replaceAll('|', '\\|').replaceAll('\r', ' ').replaceAll('\n', ' ');
}

async function main(argv) {
  const check = argv.includes('--check');
  if (check) {
    const { ok, exists } = await checkCliReference();
    if (!ok) {
      process.stderr.write(
        [
          `${CLI_REFERENCE_PATH} is ${exists ? 'out of date' : 'missing'}.`,
          'Run `npm run build && node scripts/gen-cli-reference.mjs` and commit the result.',
          '',
        ].join('\n'),
      );
      process.exitCode = 1;
    }
    return;
  }
  await writeCliReference();
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main(process.argv.slice(2)).catch((err) => {
    process.stderr.write(`gen-cli-reference: ${err.message}\n`);
    process.exitCode = 1;
  });
}
