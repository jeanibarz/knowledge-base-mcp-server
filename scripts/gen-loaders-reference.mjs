#!/usr/bin/env node
// Generate docs/reference/loaders.md from the ingest loader registry.
//
// Two registries are the single source of truth:
//   - LOADERS in src/loaders.ts: the extension -> non-text loader map. An
//     extension only appears here when a naive UTF-8 read would be wrong (PDF
//     bytes -> U+FFFD noise, HTML -> embedded markup, CSV/TSV -> rows without
//     column context, .ipynb -> one opaque JSON blob). Every other extension
//     rides the default text loader (read-as-UTF-8).
//   - INGEST_BASE_EXTENSIONS in src/ingest-filter.ts: the extensions ingested
//     by default. Extensions with a loader but NOT in this list (`.pdf`, `.csv`,
//     `.tsv`, `.ipynb`) are opt-in via `INGEST_EXTRA_EXTENSIONS`.
//
// Like the CLI and MCP-tools generators, this script imports the compiled
// registries from build/ (so it always reflects the shipped behavior) and
// writes/checks the doc. Reading the registries instead of a hand-maintained
// list is what lets the drift gate catch a newly added loader or a changed
// default. Output is deterministic (extensions sorted) so the gate is not noisy.
//
// Modes:
//   node scripts/gen-loaders-reference.mjs           # write docs/reference/loaders.md
//   node scripts/gen-loaders-reference.mjs --check   # exit 1 if the committed doc drifts
//
// Mirrors scripts/gen-cli-reference.mjs / scripts/gen-mcp-tools-doc.mjs.

import * as fs from 'node:fs/promises';
import * as path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

import { LOADERS } from '../build/loaders.js';
import { INGEST_BASE_EXTENSIONS } from '../build/ingest-filter.js';

export const LOADERS_REFERENCE_PATH = 'docs/reference/loaders.md';

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

// Human-facing metadata for each non-text loader, keyed by the loader function's
// name (LOADERS stores function references, not labels). Keying on the function
// name keeps the doc registry-derived: if a new loader is added to LOADERS with
// a function this map does not describe, generation throws instead of silently
// emitting a blank row — the drift the gate exists to catch.
const LOADER_METADATA = {
  loadPdf: {
    loader: 'pdf-parse',
    chunking: 'Extracted text stream; split by the default text splitter.',
  },
  loadHtml: {
    loader: 'html-to-text',
    chunking: 'Tags stripped to readable text (links/images dropped); split by the default text splitter.',
  },
  loadCsv: {
    loader: 'delimited-table (comma)',
    chunking: 'Rows grouped into chunk-sized blocks, each repeating the column header.',
  },
  loadTsv: {
    loader: 'delimited-table (tab)',
    chunking: 'Rows grouped into chunk-sized blocks, each repeating the column header.',
  },
  loadIpynb: {
    loader: 'ipynb',
    chunking: 'One labelled block per notebook cell; split by the default text splitter.',
  },
};

const TEXT_LOADER = {
  loader: 'text (raw UTF-8)',
  chunking: 'Read verbatim as UTF-8; split by the default text splitter.',
};

function loaderMetadataFor(ext) {
  const loader = LOADERS[ext];
  const meta = LOADER_METADATA[loader.name];
  if (!meta) {
    throw new Error(
      `No documentation metadata for loader \`${loader.name}\` (extension \`${ext}\`). ` +
        'Add an entry to LOADER_METADATA in scripts/gen-loaders-reference.mjs.',
    );
  }
  return meta;
}

export function generateLoadersReferenceMarkdown() {
  const base = new Set(INGEST_BASE_EXTENSIONS);
  const extensions = [...new Set([...INGEST_BASE_EXTENSIONS, ...Object.keys(LOADERS)])].sort(
    (a, b) => a.localeCompare(b, 'en'),
  );

  const rows = extensions.map((ext) => {
    const structured = Object.prototype.hasOwnProperty.call(LOADERS, ext);
    const meta = structured ? loaderMetadataFor(ext) : TEXT_LOADER;
    const defaultEnabled = base.has(ext);
    return [
      `\`${ext}\``,
      meta.loader,
      structured ? 'yes' : 'no',
      defaultEnabled ? 'yes' : 'no',
      meta.chunking,
    ];
  });

  const structuredCount = extensions.filter((ext) =>
    Object.prototype.hasOwnProperty.call(LOADERS, ext),
  ).length;

  const lines = [
    '# Loader Reference',
    '',
    '<!-- This file is generated by scripts/gen-loaders-reference.mjs. Do not edit by hand. -->',
    '',
    'This reference is generated from the `LOADERS` registry in `src/loaders.ts`',
    '(extension → loader) and the `INGEST_BASE_EXTENSIONS` allowlist in',
    '`src/ingest-filter.ts` (which extensions are ingested by default). Run',
    '`npm run docs:gen-loaders` after changing a loader or the default allowlist,',
    'and commit the result. The `docs:check-loaders` gate (part of `npm run check`)',
    'fails if this file drifts.',
    '',
    'Columns:',
    '',
    '- **Extension** — the lowercased file extension, matched case-insensitively.',
    '- **Loader** — `text (raw UTF-8)` is the default; a named loader means the',
    '  format is decoded to plain text before chunking.',
    '- **Structured?** — `yes` when a dedicated loader replaces the raw-text read.',
    '- **Default-enabled?** — `yes` when the extension is in `INGEST_BASE_EXTENSIONS`',
    '  and ingested without extra configuration. `no` means opt in with',
    '  `INGEST_EXTRA_EXTENSIONS` (e.g. `INGEST_EXTRA_EXTENSIONS=.pdf`).',
    '- **Chunking note** — how the loader shapes text before the splitter runs.',
    '',
    `${extensions.length} extensions have explicit handling ` +
      `(${structuredCount} with a dedicated loader). Any other plain-text extension ` +
      'opted in via `INGEST_EXTRA_EXTENSIONS` rides the default text loader.',
    '',
    '| Extension | Loader | Structured? | Default-enabled? | Chunking note |',
    '| --- | --- | --- | --- | --- |',
    ...rows.map((cells) => `| ${cells.map(tableCell).join(' | ')} |`),
  ];

  return `${lines.join('\n')}\n`;
}

export async function writeLoadersReference({ root = REPO_ROOT } = {}) {
  const target = path.join(root, LOADERS_REFERENCE_PATH);
  await fs.mkdir(path.dirname(target), { recursive: true });
  await fs.writeFile(target, generateLoadersReferenceMarkdown(), 'utf8');
}

export async function checkLoadersReference({ root = REPO_ROOT } = {}) {
  const expected = generateLoadersReferenceMarkdown();
  const target = path.join(root, LOADERS_REFERENCE_PATH);
  let actual = null;
  try {
    actual = await fs.readFile(target, 'utf8');
  } catch (err) {
    if (err.code !== 'ENOENT') throw err;
  }
  return { ok: actual === expected, exists: actual !== null };
}

function tableCell(value) {
  return String(value).replaceAll('|', '\\|').replaceAll('\r', ' ').replaceAll('\n', ' ');
}

async function main(argv) {
  if (argv.includes('--check')) {
    const { ok, exists } = await checkLoadersReference();
    if (!ok) {
      process.stderr.write(
        [
          `${LOADERS_REFERENCE_PATH} is ${exists ? 'out of date' : 'missing'}.`,
          'Run `npm run docs:gen-loaders` and commit the result.',
          '',
        ].join('\n'),
      );
      process.exitCode = 1;
    }
    return;
  }
  await writeLoadersReference();
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main(process.argv.slice(2)).catch((err) => {
    process.stderr.write(`gen-loaders-reference: ${err.message}\n`);
    process.exitCode = 1;
  });
}
