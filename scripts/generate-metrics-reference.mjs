#!/usr/bin/env node
import * as fs from 'node:fs/promises';
import * as path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

export const METRICS_REFERENCE_PATH = 'docs/reference/metrics.md';

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

export async function generateMetricsReferenceMarkdown({ root = REPO_ROOT } = {}) {
  const { OPEN_METRICS_REFERENCE } = await import(pathToFileURL(path.join(root, 'build/prometheus-export.js')).href);
  const rows = OPEN_METRICS_REFERENCE.map((metric) => [
    code(metric.name),
    code(metric.type),
    metric.labels.length === 0 ? '_none_' : metric.labels.map((label) => code(label)).join(', '),
    metric.help,
    metric.emittedWhen,
  ]);

  return `${[
    '# Metrics Reference',
    '',
    '<!-- This file is generated by scripts/generate-metrics-reference.mjs. Do not edit by hand. -->',
    '',
    'This reference is generated from `OPEN_METRICS_REFERENCE` in `src/prometheus-export.ts`.',
    'Run `npm run docs:generate-metrics-reference` after changing exported OpenMetrics families.',
    '',
    'Counter metric families omit the `_total` suffix in OpenMetrics `# TYPE` lines, but the emitted sample names keep the suffix shown below.',
    'Histogram rows list the base family name; scrapes emit `_bucket`, `_sum`, and `_count` samples for each observed label set.',
    '',
    '## Metric Families',
    '',
    '| Name | Type | Labels | Help | Emitted when |',
    '|---|---|---|---|---|',
    ...rows.map((row) => `| ${row.map(markdownTableCell).join(' | ')} |`),
  ].join('\n')}\n`;
}

export async function writeMetricsReference({ root = REPO_ROOT } = {}) {
  const target = path.join(root, METRICS_REFERENCE_PATH);
  await fs.mkdir(path.dirname(target), { recursive: true });
  await fs.writeFile(target, await generateMetricsReferenceMarkdown({ root }), 'utf8');
}

export async function checkMetricsReference({ root = REPO_ROOT } = {}) {
  const expected = await generateMetricsReferenceMarkdown({ root });
  const target = path.join(root, METRICS_REFERENCE_PATH);
  let actual = null;
  try {
    actual = await fs.readFile(target, 'utf8');
  } catch (err) {
    if (err.code !== 'ENOENT') throw err;
  }
  return { ok: actual === expected, exists: actual !== null };
}

function code(value) {
  return `<code>${escapeHtml(value)}</code>`;
}

function escapeHtml(value) {
  return value
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;');
}

function markdownTableCell(value) {
  return value.replaceAll('|', '\\|').replaceAll('\r', ' ').replaceAll('\n', ' ');
}

async function main(argv) {
  if (argv.includes('--check')) {
    const { ok, exists } = await checkMetricsReference();
    if (!ok) {
      process.stderr.write(
        [
          `${METRICS_REFERENCE_PATH} is ${exists ? 'out of date' : 'missing'}.`,
          'Run `npm run build && npm run docs:generate-metrics-reference` and commit the result.',
          '',
        ].join('\n'),
      );
      process.exitCode = 1;
    }
    return;
  }
  await writeMetricsReference();
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main(process.argv.slice(2)).catch((err) => {
    process.stderr.write(`docs:generate-metrics-reference: ${err.message}\n`);
    process.exitCode = 1;
  });
}
