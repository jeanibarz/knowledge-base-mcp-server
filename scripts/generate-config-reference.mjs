#!/usr/bin/env node
import * as fs from 'node:fs/promises';
import * as path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

import { CONFIG_SCHEMA } from '../build/config/schema.js';

export const CONFIG_REFERENCE_PATH = 'docs/reference/configuration.md';

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

export function generateConfigReferenceMarkdown(schema = CONFIG_SCHEMA) {
  const rows = schema.map((spec) => [
    code(spec.name),
    code(spec.kind),
    defaultForSpec(spec),
    acceptedValuesForSpec(spec),
    constraintsForSpec(spec),
    emptyValueForSpec(spec),
    spec.description ?? '',
  ]);

  return `${[
    '# Configuration Reference',
    '',
    '<!-- This file is generated by scripts/generate-config-reference.mjs. Do not edit by hand. -->',
    '',
    'This reference is generated from `CONFIG_SCHEMA` in `src/config/schema.ts`.',
    'Run `npm run docs:generate-config-reference` after changing the schema.',
    '',
    '| Name | Kind | Default | Accepted values | Constraints | Empty value | Description |',
    '|---|---|---|---|---|---|---|',
    ...rows.map((row) => `| ${row.map(markdownTableCell).join(' | ')} |`),
  ].join('\n')}\n`;
}

export async function writeConfigReference({ root = REPO_ROOT } = {}) {
  const target = path.join(root, CONFIG_REFERENCE_PATH);
  await fs.mkdir(path.dirname(target), { recursive: true });
  await fs.writeFile(target, generateConfigReferenceMarkdown(), 'utf8');
}

function defaultForSpec(spec) {
  if (spec.docDefault !== undefined) return code(spec.docDefault);
  if (spec.default !== undefined) return spec.default === '' ? '_empty string_' : code(spec.default);
  return '_unset_';
}

function acceptedValuesForSpec(spec) {
  if (spec.kind === 'enum') return listCode(spec.values);
  if (spec.booleanValues !== undefined) return listCode(spec.booleanValues);
  if (spec.kind === 'boolean') return listCode(['on', 'off', 'true', 'false', '1', '0']);
  if (spec.protocols !== undefined) return `protocols: ${listCode(spec.protocols)}`;
  if (spec.kind === 'csv') return 'comma-separated strings';
  return '';
}

function constraintsForSpec(spec) {
  const constraints = [];
  if (spec.min !== undefined) constraints.push(`>= ${code(String(spec.min))}`);
  if (spec.max !== undefined) constraints.push(`<= ${code(String(spec.max))}`);
  if (spec.integerSyntax === 'digits') constraints.push('digits only');
  if (spec.secret === true || spec.kind === 'secret') constraints.push('redacted in reports');
  return constraints.join('; ');
}

function emptyValueForSpec(spec) {
  if (spec.emptyUsesDefault === true || (spec.kind !== 'string' && spec.kind !== 'secret')) {
    return 'uses default';
  }
  return spec.default === undefined ? 'kept as set' : 'kept as empty';
}

function listCode(values) {
  return values.map((value) => code(String(value))).join(', ');
}

function code(value) {
  return `<code>${escapeHtml(value)}</code>`;
}

function escapeHtml(value) {
  return value
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;');
}

function markdownTableCell(value) {
  return value.replaceAll('|', '\\|').replaceAll('\r', ' ').replaceAll('\n', ' ');
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  writeConfigReference().catch((err) => {
    process.stderr.write(`docs:generate-config-reference: ${err.message}\n`);
    process.exitCode = 1;
  });
}
